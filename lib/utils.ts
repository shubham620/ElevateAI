export function isOverdue(deadline: Date | string): boolean {
  return new Date(deadline) < new Date()
}

export function getDaysUntilDeadline(deadline: Date | string): number {
  const today = new Date()
  const dead = new Date(deadline)
  const diff = dead.getTime() - today.getTime()
  return Math.ceil(diff / (1000 * 60 * 60 * 24))
}

export function getCurrentQuarter(): string {
  const month = new Date().getMonth() + 1
  if (month <= 3) return "Q1"
  if (month <= 6) return "Q2"
  if (month <= 9) return "Q3"
  return "Q4"
}

export function getCurrentYear(): number {
  return new Date().getFullYear()
}

export function validateWeightage(weightages: number[]): string {
  const sum = weightages.reduce((a, b) => a + b, 0)
  if (sum !== 100) {
    return `Total weightage must be 100%, currently ${sum}%`
  }
  for (const w of weightages) {
    if (w < 10) {
      return "Each goal must have at least 10% weightage"
    }
  }
  return ""
}

export function truncate(str: string, length: number): string {
  return str.length > length ? str.substring(0, length) + "..." : str
}
