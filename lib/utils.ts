export function calculateWeightageSum(goals: any[]): number {
  return goals.reduce((sum, goal) => sum + (goal.weightage || 0), 0)
}

export function calculateHealthScore(metrics: {
  consistency: number
  completion: number
  engagement: number
  trend: number
  discipline: number
}): number {
  return Math.round(
    (metrics.consistency * 0.2 +
      metrics.completion * 0.3 +
      metrics.engagement * 0.2 +
      metrics.trend * 0.15 +
      metrics.discipline * 0.15) /
      100
  )
}

